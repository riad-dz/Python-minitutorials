## Function for uppercase ###

sentence="i really love pizza"
print (sentence.upper())


## Function for lowercase ###

sentence="I REALLY LOVE PIZZA"
print (sentence.lower())

## Function to verify is sentence is written with lowercase ###

sentence="I REALLY LOVE PIZZA"
print (sentence.islower())


## Force sentence in lowercase and verify if boolean is true ###

sentence="I REALLY LOVE PIZZA"
print (sentence.lower().islower())


## Output the length of the sentence ###

sentence="I REALLY LOVE PIZZA"
print(len(sentence))

## Replace the word pizza by hamburgers ###
sentence="I REALLY LOVE PIZZA"
print(sentence.replace("PIZZA","hamburgers"))


## index of a particular letter in the sentence #####
sentence="I REALLY LOVE PIZZA"
print(sentence.index("Z"))

## Sentence with line break ##
print("i love pizzas \n hamburgers \n pastas")



